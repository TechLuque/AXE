<script setup>
import { onMounted } from 'vue'

onMounted(() => {
  const navItems = document.querySelectorAll('.nav-item')
  const indicator = document.querySelector('.selection-indicator')

  function updateIndicatorPosition() {
    const activeItem = document.querySelector('.nav-item.active')
    if (activeItem) {
      indicator.style.width = `${activeItem.offsetWidth - 20}px`
      indicator.style.left = `${activeItem.offsetLeft + activeItem.offsetWidth / 2}px`
      indicator.style.transform = 'translateX(-50%)'
    }
  }

  function updateActiveOnScroll() {
    const sections = [
      { id: '#auditorio', item: navItems[0] },
      { id: '#speakers', item: navItems[1] },
      { id: '#cronograma', item: navItems[2] }
    ]

    let currentSection = null
    
    sections.forEach(({ id, item }) => {
      const element = document.querySelector(id)
      if (element) {
        const rect = element.getBoundingClientRect()
        if (rect.top <= window.innerHeight / 2) {
          currentSection = item
        }
      }
    })

    if (currentSection) {
      navItems.forEach(i => i.classList.remove('active'))
      currentSection.classList.add('active')
      updateIndicatorPosition()
    }
  }

  navItems.forEach(item => {
    item.addEventListener('click', e => {
      const href = item.querySelector('a')?.getAttribute('href')
      
      // Solo prevenir el comportamiento por defecto para enlaces internos
      if (href && href.startsWith('#')) {
        e.preventDefault()
        navItems.forEach(i => i.classList.remove('active'))
        item.classList.add('active')
        updateIndicatorPosition()
        
        const target = document.querySelector(href)
        if (target) {
          target.scrollIntoView({ behavior: 'smooth' })
        }
      } else {
        // Para enlaces externos, permitir que se abran normalmente
        navItems.forEach(i => i.classList.remove('active'))
        item.classList.add('active')
        updateIndicatorPosition()
      }
    })
  })

  window.addEventListener('resize', updateIndicatorPosition)
  window.addEventListener('scroll', updateActiveOnScroll)
  updateIndicatorPosition()
})
</script>

